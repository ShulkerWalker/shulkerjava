public class whi {
	public static void main (String[] args) {
		int i = 10;
		while (i <= 20) {
			System.out.println(i);
			i ++;
		}
		int g = 10;
		while (g >= 1) {
			System.out.println(g);
			g --;
		}
		int sum = 0;
		int h = 1;
		while (h <= 1000) {
			sum = sum + h;
			h ++;
		}
		System.out.println(sum);
	}
}
